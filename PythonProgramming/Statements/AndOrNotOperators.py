
# And operator

print(1 < 2 and "robert" == "robert")

print(1 > 2 and "robert" == "robert")

print(1 < 2 and "car" < "zero" and 4.32 < 5.41)

# or operator

print(1 > 2 or "robert" == "robert")

print(1 < 2 or "car" < "zero" or 4.32 < 5.41)

print(1 > 2 or "robert" != "robert")

# not operator

print(not 2 == 2)
